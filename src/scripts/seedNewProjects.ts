// Mirrors the afterRead conversion in src/collections/Projects.tsx so
// plain multi-paragraph strings become valid Lexical richText values.
function convertTextToLexical(text: string) {
  const paragraphs = text.split("\n").filter((p) => p.trim());

  const children = paragraphs.map((paragraph) => ({
    children: [
      {
        detail: 0,
        format: 0,
        mode: "normal",
        style: "",
        text: paragraph.trim(),
        type: "text",
        version: 1,
      },
    ],
    direction: "ltr",
    format: "",
    indent: 0,
    type: "paragraph",
    version: 1,
  }));

  return {
    root: {
      type: "root",
      format: "",
      indent: 0,
      version: 1,
      children,
      direction: "ltr",
    },
  } as any;
}

